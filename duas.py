def get_morning_dua():
    return """
🌅 Morning Azkar

اللَّهُمَّ بِكَ أَصْبَحْنَا، وَبِكَ أَمْسَيْنَا، وَبِكَ نَحْيَا، وَبِكَ نَمُوتُ، وَإِلَيْكَ النُّشُورُ.

Allahumma bika asbahna, wa bika amsayna, wa bika nahya, wa bika namutu, wa ilaykan-nushoor.

O Allah, by You we enter the morning, by You we enter the evening, by You we live, by You we die, and to You is the resurrection.
"""


def get_evening_dua():
    return """
🌙 Evening Azkar

اللَّهُمَّ بِكَ أَمْسَيْنَا، وَبِكَ أَصْبَحْنَا، وَبِكَ نَحْيَا، وَبِكَ نَمُوتُ، وَإِلَيْكَ الْمَصِيرُ.

Allahumma bika amsayna, wa bika asbahna, wa bika nahya, wa bika namutu, wa ilaykal-maseer.

O Allah, by You we enter the evening, by You we enter the morning, by You we live, by You we die, and to You is the final return.
"""


def get_sleep_dua():
    return """
😴 Before Sleeping

بِاسْمِكَ اللَّهُمَّ أَمُوتُ وَأَحْيَا.

Bismika Allahumma amutu wa ahya.

In Your name, O Allah, I die and I live.
"""


def get_friday_dua():
    return """
🤲 Jumu'ah Dua

اللَّهُمَّ اغْفِرْ لَنَا ذُنُوبَنَا، وَافْتَحْ لَنَا أَبْوَابَ رَحْمَتِكَ، وَارْزُقْنَا مِنْ حَيْثُ لَا نَحْتَسِبُ.

Allahumma ighfir lana dhunubana, waftah lana abwaba rahmatika, warzuqna min haythu la nahtasib.

O Allah, forgive our sins, open for us the doors of Your mercy, and provide for us from places we could never imagine.


اللَّهُمَّ اجْعَلْ هَذَا الْيَوْمَ يَوْمَ خَيْرٍ وَبَرَكَةٍ وَنُورٍ وَرَحْمَةٍ.

Allahumma aj'al hadhal yawma yawma khayrin wa barakatin wa nurin wa rahmah.

O Allah, make this Friday a day of goodness, blessings, light, and mercy.


اللَّهُمَّ صَلِّ وَسَلِّمْ عَلَى نَبِيِّنَا مُحَمَّدٍ ﷺ.

Allahumma salli wa sallim 'ala Nabiyyina Muhammad ﷺ.

O Allah, send Your peace and blessings upon our beloved Prophet Muhammad ﷺ.


🤍 May Allah accept all our du'as, forgive our shortcomings, bless our hearts with peace, and reunite us in Jannatul Firdaus.

Ameen.
"""